const countTotalCost = () => {
	const allAddedCases = document.querySelectorAll(".battles__item");
	let cost = 0;

	allAddedCases.forEach((item) => {
		cost =
			cost +
			parseFloat(item.children[2].textContent) *
				parseInt(item.children[3].children[1].textContent);
	});

	totalCostOfCases.textContent = cost.toFixed(2) + "$";
};

function changeCasesAmount() {
	let amount = parseInt(this.parentElement.children[1].textContent);

	if (
		this.classList.contains("battles__item-btn-minus") &&
		parseInt(this.parentElement.children[1].textContent) > 1
	) {
		amount = amount - 1;
		this.parentElement.children[1].textContent = amount;
	} else if (this.classList.contains("battles__item-btn-plus")) {
		amount = amount + 1;
		this.parentElement.children[1].textContent = amount;
	}

	countTotalCost();
}

